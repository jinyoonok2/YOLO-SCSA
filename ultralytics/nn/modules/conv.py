# Larger Sigma
# Results in a broader, more gradual peak.
# The values decrease more slowly from the center to the edges.
# Distributes attention over a wider central area.

# Non-linearity(pow):
# Flattens the curve more significantly.
# Increases the values in the lower range more than those in the higher range.
# Results in a broader region of high attention values, making the transition from the center to the edges more gradual.

# Increasing min_scale:
# Setting a higher min_scale raises the baseline of the attention map.
# This means the lowest attention value is higher, but the shape of the Gaussian distribution remains unchanged.

# Ensure that the attention map in CentralFocusAttention also uses float16
class CenterWeightedMask(nn.Module):
    def __init__(self, channel=512, sigma=0.5, pow_val=1.0, min_scale=0.9, max_scale=1.0):
        super(CenterWeightedMask, self).__init__()
        self.sigma = sigma  # This is where the sigma value is set
        self.pow_val = pow_val
        self.min_scale = min_scale
        self.max_scale = max_scale

    def create_attention_map(self, shape):
        # Create a Gaussian-like attention map centered in the middle of the input
        _, _, h, w = shape
        x = torch.linspace(-1, 1, w).view(1, 1, 1, w).type(torch.float32)
        y = torch.linspace(-1, 1, h).view(1, 1, h, 1).type(torch.float32)
        x = x ** 2
        y = y ** 2

        # Gaussian function
        # Using the sigma value to control the spread of the Gaussian
        attention_map = torch.exp(-(x + y) / (2 * self.sigma ** 2))

        # Non-linear adjustment
        attention_map = torch.pow(attention_map, self.pow_val)  # No adjustment since pow_val=1.0

        # Scale the attention map from min_scale to max_scale
        attention_map = self.min_scale + (self.max_scale - self.min_scale) * attention_map
        return attention_map

    def forward(self, x):
        if not hasattr(self, 'attention_map') or self.attention_map.size() != x.size():
            self.attention_map = self.create_attention_map(x.size()).to(x.device).type(torch.float16)
        return x * self.attention_map
class CenterWeightedConcat(nn.Module):
    """Concatenate a list of tensors along dimension and apply central focus attention."""

    def __init__(self, dimension=1):
        """Concatenates a list of tensors along a specified dimension."""
        super().__init__()
